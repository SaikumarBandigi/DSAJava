public class DiwaliGreetings {
    public static void main(String[] args) {
        printHappyDiwali();
    }

    private static void printHappyDiwali() {
        printH();
        printA();
        printP();
        printP();
        printY();

        System.out.println(); // Empty line

        printD();
        printI();
        printW();
        printA();
        printL();
        printI();
    }

    private static void printH() {
        System.out.println("H   H  AAAAA  PPPPP  PPPPP  Y   Y");
        System.out.println("H   H  A   A  P   P  P   P   Y Y ");
        System.out.println("HHHHH  AAAAA  PPPPP  PPPPP    Y  ");
        System.out.println("H   H  A   A  P      P      Y   Y");
        System.out.println("H   H  A   A  P      P      Y   Y");
    }

    private static void printA() {
        System.out.println("   A   ");
        System.out.println("  A A  ");
        System.out.println(" A   A ");
        System.out.println("AAAAA  ");
        System.out.println("A   A  ");
    }

    private static void printP() {
        System.out.println("PPPP  ");
        System.out.println("P   P ");
        System.out.println("PPPP  ");
        System.out.println("P     ");
        System.out.println("P     ");
    }

    private static void printY() {
        System.out.println("Y   Y  ");
        System.out.println(" Y Y   ");
        System.out.println("  Y    ");
        System.out.println("  Y    ");
        System.out.println("  Y    ");
    }

    private static void printD() {
        System.out.println("DDDD   ");
        System.out.println("D   D  ");
        System.out.println("D    D ");
        System.out.println("D   D  ");
        System.out.println("DDDD   ");
    }

    private static void printI() {
        System.out.println("IIII  ");
        System.out.println(" III  ");
        System.out.println(" III  ");
        System.out.println(" III  ");
        System.out.println("IIII  ");
    }

    private static void printW() {
        System.out.println("W   W  ");
        System.out.println("W   W  ");
        System.out.println("W W W  ");
        System.out.println("W W W  ");
        System.out.println(" W W   ");
    }

    private static void printL() {
        System.out.println("L      ");
        System.out.println("L      ");
        System.out.println("L      ");
        System.out.println("L      ");
        System.out.println("LLLLL  ");
    }
}
